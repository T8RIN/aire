package com.awxkee.aire

data class KernelShape(val width: Int, val height: Int) {
    init {
        if (width % 2 == 0) {
            throw IllegalStateException("Kernel shape must be always odd")
        }
        if (height % 2 == 0) {
            throw IllegalStateException("Kernel shape must be always odd")
        }
    }
}
